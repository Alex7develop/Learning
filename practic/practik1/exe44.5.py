# Получить ввод от пользователя
string1 = input("Введите первую строку: ")
string2 = input("Введите вторую строку: ")

# Разделить строки на слова
words1 = string1.split()
words2 = string2.split()

# Обменять местами слова с четными номерами
for i in range(1, len(words1), 2):
    if i < len(words2):
        words1[i], words2[i] = words2[i], words1[i]

# Объединить слова обратно в строки
new_string1 = " ".join(words1)
new_string2 = " ".join(words2)

# Вывести результат
print("Новая первая строка:", new_string1)
print("Новая вторая строка:", new_string2)

# Даны две строки со словами. Обменять
# местами слова с четными номерами в этих строках


#В этом коде мы сначала получаем ввод от
# пользователя и разделяем строки на слова
# с помощью метода split(). Затем мы используем
# цикл for для обмена местами слов с четными
# номерами в обеих строках. Для этого мы используем
# оператор множественного присваивания, чтобы
# одновременно поменять местами два элемента списка.
# После этого мы объединяем слова обратно в строки
# с помощью метода join() и выводим результаты.